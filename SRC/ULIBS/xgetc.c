



#include "xgetc.h"

//------------------------------------------------------------------------------
int xgetc( char *c )
{
    
    if ( frtos_read( fdTERM, c, 1 ) == 1 )
        return(1);
    
    return(0);
    
}
//------------------------------------------------------------------------------
int xfgetc( int fd, char *c )
{
    if ( frtos_read( fd, c, 1 ) == 1 )
        return(1);
    
    return(0);    
    
}
